#DrawPoint-function--------------------------------------------------------------

def drawPoint(iter):
	x = int(input("x" + str(iter) + ": "))
	y = int(input("y" + str(iter) + ": "))

	xy = ((y - 1) * 50) + (x - iter)

	global newGrid

	newGrid = newGrid.replace("□", str(iter), xy)
	newGrid = newGrid.replace(str(iter), "□", (xy - 1))

	newGrid = newGrid.replace(str(iter), "■")

	return newGrid

#Draw-points---------------------------------------------------------------------


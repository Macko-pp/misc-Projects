#Draw0point-function-------------------------------------------------------------

def draw0Point(x, y):
	# x0 = int(input("x0: "))
	# y0 = int(input("y0: "))

	xy = ((y - 1) * 50) + x

	global newGrid

	newGrid = blankSquareGrid.replace("□", "■", xy)
	newGrid = newGrid.replace("■", "□", (xy - 1))

	# return newGrid

#drawPoint-function--------------------------------------------------------------

def drawPoint(x, y, iter):
	# x = int(input(f'x{iter}": '))
	# y = int(input(f'y{iter}": '))

	xy = ((y - 1) * 50) + (x - iter)

	global newGrid

	newGrid = newGrid.replace("□", str(iter), xy)
	newGrid = newGrid.replace(str(iter), "□", (xy - 1))

	newGrid = newGrid.replace(str(iter), "■")

	# return newGrid

#drawLine-function---------------------------------------------------------------

def drawLine(x_start, y_start, x_end, y_end):
	dy = y_end - y_start
	dx = x_end - x_start

	error = 2*dy - dx
	y = y_start
	for x in range(x_start, x_end + 1):
		print(f'{x} - {y}')

		if "newGrid" in globals():
			drawPoint(x, y, x - x_start)
		else:
			draw0Point(x, y)

		if error >= 0:
			y += 1
			error -= 2*dx

		error += 2*dy

